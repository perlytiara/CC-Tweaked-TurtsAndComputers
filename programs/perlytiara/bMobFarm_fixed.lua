local function water()
  for i=1,8 do gf() end
  gf() gl() ss(12) pf() gr() gr() 
  ss(13) pf() gl() gb() os.sleep(0.8) 
  pf() os.sleep(0.8) ss(12) pf()
  gl() gl() gf() gf() gf() gf() gf() sf() sf()
  gf() gf() gf()
end

local function getTorches()
  ss(12) df() pf() os.sleep(0.3) pf() gf() gl()
  for i=1,11 do gf() end
end

